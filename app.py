import streamlit as st
import pandas as pd
import joblib
import itertools
import datetime as dt
import os

# Load both models and the transformer
model1 = joblib.load('XGBRegressor_MultiOutputRegressor_model.joblib')
model2 = joblib.load('XGBRegressor_RegressorChain_model.joblib')
ct = joblib.load('ct.joblib')

columns = ['airline', 'from', 'stop', 'to', 'class', 'time_of_day', 'is_weekday', 'month']

st.subheader("✈️ Flight Price + Time Predictions")

# Model selection sidebar
selected_model_name = st.sidebar.radio(
    "Choose Prediction Model",
    ["MultiOutputRegressor (Model 1)", "RegressorChain (Model 2)"]
)
st.sidebar.markdown("___")
st.sidebar.write("Predicting Flight Duration and Price using optuna for hyperparameter tuning, mlflow for logging metrics and artifacts and streamlit for deployment.\n")
st.sidebar.write(f"About time_of_day \n 5 am - 9 am early morning,\n 9 am - 12 pm morning,\n 12 pm - 4 pm afternoon,\n 4 pm - 8 pm evening,\n 8 pm - 5 am night")


active_model = model1 if selected_model_name == "MultiOutputRegressor (Model 1)" else model2

selected_date = st.date_input("Pick a date")
fro = st.selectbox("From", ['Delhi', 'Mumbai', 'Bangalore', 'Kolkata', 'Hyderabad', 'Chennai'])
to = ['Mumbai', 'Bangalore', 'Kolkata', 'Hyderabad', 'Chennai', 'Delhi']

if fro in to:
    to.remove(fro)
to = st.selectbox("To City", to)

is_weekday = selected_date.weekday()
month = selected_date.month

airlines = st.multiselect(
    "Select Airlines", 
    ["Vistara", "Air India", "Indigo", "SpiceJet", "GO FIRST", "AirAsia", "StarAir", "Trujet"],
    default=["Vistara"]
)
if len(airlines) == 0:
    st.warning("Please select at least one airline.")
    st.stop()

stop = st.multiselect(
    "Select Stops", 
    ["non-stop", "1-stop", "1-stopVia", "2+-stop"], 
    default=["non-stop"]
)
if len(stop) == 0:
    st.warning("Please select at least one stop option.")
    st.stop()

clss = st.multiselect(
    "Select Class", 
    ["economy", "business"], 
    default=["economy"]
)
if len(clss) == 0:
    st.warning("Please select at least one class.")
    st.stop()

time_of_day = st.multiselect(
    "Select Time of Day", 
    ["evening", "early morning", "night", "morning", "afternoon"], 
    default=["evening"]
)
if len(time_of_day) == 0:
    st.warning("Please select at least one time of day.")
    st.stop()

data = {
    'airline': airlines,
    'from': [fro],
    'stop': stop,
    'to': [to],
    'class': clss,
    'time_of_day': time_of_day,
    'is_weekday': [is_weekday],
    'month': [month]
}

combinations = list(itertools.product(*data.values()))
x_df = pd.DataFrame(combinations, columns=data.keys())

def predict(data, current_model):
    X = ct.transform(data)
    predictions = current_model.predict(X)
    return predictions

preds = predict(x_df, active_model)

# price
x_df["Predicted_Price"] = preds[:, 1]

# time
time = preds[:, 0] / 60
hour = time.astype(int)
minute = ((time - hour) * 60).round().astype(int)
hour += (minute == 60)
minute = minute % 60
x_df["Predicted_Time"] = [
    f"{h}:{m:02d}" for h, m in zip(hour, minute)
]

# average
avg_price = x_df["Predicted_Price"].mean()
min_price = x_df["Predicted_Price"].min()
avg_time = time.mean()
min_time = time.min()

avg_hour = int(avg_time)
avg_minute = round((avg_time - avg_hour) * 60)
if avg_minute == 60:
    avg_hour += 1
    avg_minute = 0

min_hour = int(min_time)
min_minute = round((min_time - min_hour) * 60)
if min_minute == 60:
    min_hour += 1
    min_minute = 0

x_df.drop(["is_weekday", "month", "class"], axis=1, inplace=True)

col1, col2, col3, col4 = st.columns(4)
with col1:
    st.metric(label="💵 Avg Predicted Price", value=f"₹{avg_price:,.2f}")
with col2:
    st.metric(label="🏷️ Lowest Predicted Price", value=f"₹{min_price:,.2f}")
with col3:
    st.metric(label="⏱️ Avg Flight Duration", value=f"{avg_hour}:{avg_minute:02d}")
with col4:
    st.metric(label="⚡ Fastest Flight Duration", value=f"{min_hour}:{min_minute:02d}")

st.markdown("---")
st.caption(f"Currently showing data generated by: **{selected_model_name}**")
x_df["Model"]  = selected_model_name
if "Rating" not in x_df.columns:
    x_df["Rating"] = 0


edited_df = st.data_editor(
    x_df,
    use_container_width=True,
    hide_index=True,
    column_config={
        "Rating": st.column_config.SelectboxColumn(
            "⭐ Rating",
            options=[1, 2, 3, 4, 5],
            required=True,
        )
    },
    key="prediction_table",
)

if st.button("💾 Save Reviews"):
    file_path = "prediction_reviews.csv"

    if os.path.exists(file_path):
        old_df = pd.read_csv(file_path)
        final_df = pd.concat([old_df, edited_df], axis=0, ignore_index=True)
    else:
        final_df = edited_df

    final_df.to_csv(file_path, index=False)
    st.success("Reviews saved successfully!")
st.write("average price by airline")
price_by_airline = (
    x_df.groupby("airline")["Predicted_Price"]
    .mean()
)
st.bar_chart(price_by_airline, use_container_width=True, x_label="airline", y_label="Average Predicted_Price")
